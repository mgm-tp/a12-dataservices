	private String getModelStringContent(String modelId) {
		return genericModelLoader.loadModel(modelId)
			.getContent()
			.getRawContent()
			.replace("\r\n", "\n");
	}

	private static class BulkImporterConfigurationBuilder {
		private final BulkImporterConfiguration config = new BulkImporterConfiguration();

		public BulkImporterConfigurationBuilder overwriteModelsDefault(boolean value) {
			config.setOverwriteModelsDefault(value);
			return this;
		}

		public BulkImporterConfigurationBuilder overwriteDocumentModels(boolean value) {
			config.setOverwriteDocumentModels(value);
			return this;
		}

		public BulkImporterConfigurationBuilder overwriteModels(String modelType, boolean value) {
			config.setOverwriteModels(modelType, value);
			return this;
		}

		public BulkImporterConfiguration build() {
			return config;
		}
	}

