	@Test(dataProvider = "multiErrorPathProvider")
	public void importWithMultipleErrors(String path) {
		log.info("Processing {}", path);

		modelService.delete("BusinessPartnerSuperInclude4");

		ModelBulkImportException ex =
			Assert.expectThrows(ModelBulkImportException.class, () -> modelBulkImporter.doImport(path, new BulkImporterConfiguration()));

		List<BaseError> errors = ex.getErrors();
		assertContainsError(errors, ExceptionKeys.MODEL_DUPLICITY_ERROR_KEY, "Duplicate files for model BusinessPartnerSuper");
		assertContainsError(errors, ExceptionKeys.MODEL_NOT_FOUND_ERROR_KEY, "Document model [BusinessPartnerSuperInclude4] not found");
		assertContainsError(errors, ExceptionKeys.MODEL_ACCESS_DENIED_ERROR_KEY, "Access to model [Address] has been denied!");
		Assert.assertEquals(errors.size(), 3);
	}

	private void assertContainsError(List<BaseError> errors, String key, String msg) {
		Optional<BaseError> optional = errors.stream()
			.filter(e -> e.getShortMessage().getKey().equals(key))
			.findFirst();
		Assert.assertTrue(optional.isPresent(), "Exception '%s' is expected but not thrown".formatted(msg));
		Assert.assertEquals(optional.get().getShortMessage().getDefaultMessage(), msg);
	}

	@Test(dataProvider = "multiModelsPath")
	public void importWithPreventedOverwrite(String path) throws Exception {
		String modelContent = loadResourceFromClasspathAsString(BUSINESS_PARTNER_SUPER_DOCUMENT_MODEL_PATH);
		modelService.create(modelContent);
		String expectedModelContent = loadResourceFromClasspathAsString(BUSINESS_PARTNER_SUPER_WITH_METADATA_MODEL_PATH);
		JSONAssert.assertEquals(expectedModelContent,
			getModelStringContent(BUSINESS_PARTNER_SUPER_MODEL), false);
		log.info("Processing {}", path);
		BulkImporterConfiguration configuration = new BulkImporterConfiguration();
		configuration.setOverwriteDocumentModels(false);
		modelBulkImporter.doImport(path, configuration);
		JSONAssert.assertEquals(expectedModelContent, getModelStringContent(BUSINESS_PARTNER_SUPER_MODEL), false);
	}

