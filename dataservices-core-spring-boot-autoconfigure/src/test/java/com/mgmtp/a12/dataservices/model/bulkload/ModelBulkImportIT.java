	@Test(dataProvider = "chekModelTypeOverwritingProvider")
	public void chekModelTypeOverwriting(
			BulkImporterConfiguration configuration, String documentModelTypeContentExpected, String formModelTypeContentExpected
	) throws IOException, URISyntaxException {
		assertOverwrite(DOCUMENT_MODEL_TYPE_ORIGINAL_CONTENT, FORM_MODEL_TYPE_ORIGINAL_CONTENT, new BulkImporterConfiguration(), ORIGINAL_VALUE);
		assertOverwrite(documentModelTypeContentExpected, formModelTypeContentExpected, configuration, OVERWRITTEN_VALUE);
	}

	private void assertOverwrite(String addressModelContentExpected, String defaultModelContentExpected, BulkImporterConfiguration configuration, String suffix)
			throws IOException, URISyntaxException {
		modelBulkImporter.doImport(MODELS_BULKLOAD_CLASSPATH_PREFIX + "/overwriting/" + suffix, configuration);
		JSONAssert.assertEquals(addressModelContentExpected, getModelStringContent(ADDRESS_DOCUMENT_MODEL), true);
		JSONAssert.assertEquals(defaultModelContentExpected, getModelStringContent(ADDRESS_FORM_MODEL), true);
	}

	@Test(dataProvider = "multiModelsPath")
	public void testImport(String path) throws URISyntaxException, IOException {
		log.info("Processing {}", path);
		modelBulkImporter.doImport(path, new BulkImporterConfiguration());
		assertContent();
	}

	private void assertContent() {
		Assert.assertEquals(modelHeaderJpaRepository.findAll()
				.stream()
				.map(ModelHeaderEntity::getId)
				.collect(Collectors.toSet()),
			Set.of("AddressOther", "Address", "Contract", "BusinessPartnerSuperInclude1", "BusinessPartnerSuperInclude2", "BusinessPartnerSuperInclude3",
				"BusinessPartnerSuperInclude4", "BusinessPartnerSuperInclude5", "BusinessPartnerSuperOther", "BusinessPartnerSuper", "DemoModelBare",
				"DemoModelWithAllFields")
		);

		Assert.assertEquals(getModelStringContent("DemoModelBare"), loadResourceFromClasspathAsString(MODELS_BULKLOAD_DIR + "/models/jsonModelBase.json"));
		Assert.assertEquals(getModelStringContent("DemoModelWithAllFields"),
			loadResourceFromClasspathAsString(MODELS_BULKLOAD_DIR + "/models/jsonModelFull.json"));
	}

	@Test(dataProvider = "duplicatePathProvider")
	public void testImportDuplicate(String path) {
		log.info("Processing {}", path);

		ModelBulkImportException ex =
			Assert.expectThrows(ModelBulkImportException.class, () -> modelBulkImporter.doImport(path, new BulkImporterConfiguration()));

		List<BaseError> errors = ex.getErrors();
		Assert.assertFalse(errors.isEmpty());
		BaseError error = errors.get(0);
		Assert.assertTrue(error.getShortMessage().getDefaultMessage().matches("Duplicate files for model .*"));
	}

