package com.mgmtp.a12.dataservices.document.internal.entity;

import com.mgmtp.a12.dataservices.document.DocumentReference;

import jakarta.persistence.Column;
import jakarta.persistence.Convert;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.SequenceGenerator;
import jakarta.persistence.Table;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data @AllArgsConstructor @NoArgsConstructor
@SequenceGenerator(name = "document_unique_constraint_seq", sequenceName = "DOCUMENT_UNIQUE_CONSTRAINT_SEQ", allocationSize = 1)
@Table(name = DocumentUniqueConstraintEntity.TABLE_NAME)
@Entity public class DocumentUniqueConstraintEntity {

	public static final String TABLE_NAME = "DOCUMENT_UNIQUE_CONSTRAINT";

	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "document_unique_constraint_seq")
	@Id Long id;

	String modelName;

	String constraintName;

	String fieldValuesHash;

	@Convert(converter = DocumentReferenceConverter.class)
	@Column(name = "doc_ref")
	DocumentReference documentReference;
}
