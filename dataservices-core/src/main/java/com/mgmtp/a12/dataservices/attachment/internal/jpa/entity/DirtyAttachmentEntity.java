package com.mgmtp.a12.dataservices.attachment.internal.jpa.entity;

import java.io.Serializable;
import java.time.Instant;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * Contains all resources for attachment header definition.
 */
@Table(name = "DIRTY_ATTACHMENT")
@Data @NoArgsConstructor @AllArgsConstructor @Builder(toBuilder = true)
@Entity public class DirtyAttachmentEntity implements Serializable {

	@Id @Column(name = "id") private String attachmentId;

	@Column(name = "created_at") private Instant createdAt;

	@Column(name = "created_by") private String createdBy;

	@Column(name = "last_try") private Instant lastTry;

	@Column(name = "exec_count") private long execCount;

}
